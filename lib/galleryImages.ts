// Helper function to get paginated images
export function getPaginatedImages(page: number = 1, limit: number = 12) {
    const startIndex = (page - 1) * limit;
    const endIndex = startIndex + limit;
    return {
        images: galleryImages.slice(startIndex, endIndex),
        hasMore: endIndex < galleryImages.length,
        totalCount: galleryImages.length,
        currentPage: page,
        totalPages: Math.ceil(galleryImages.length / limit),
    };
}

// Helper function to get unique categories
export function getUniqueCategories() {
    const categories = new Set(galleryImages.map(img => img.category).filter(Boolean));
    return Array.from(categories) as string[];
}